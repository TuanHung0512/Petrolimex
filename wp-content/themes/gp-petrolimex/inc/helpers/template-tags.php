function gp_petrolimex_get_share_post_link($post, $type)
{
	$post_link = get_permalink($post->ID);

	switch ($type) {
		case 'twitter';
			return sprintf('https://twitter.com/share?url=%s', get_permalink($post->ID));
			break;

		case 'pinterest':
			return sprintf(
				'https://pinterest.com/pin/create/button/?url=%1$s&amp;media=%2$s&amp;description=%3$s',
				$post_link,
				(has_post_thumbnail($post) ? wp_get_attachment_image_url(get_post_thumbnail_id($post->ID)) : ''),
				get_the_title($post->ID)
			);
			break;

		case 'facebook':
			return sprintf(
				'https://www.facebook.com/sharer.php?u=%1$s',
				$post_link
			);
			break;

		case 'linkedin':
			return sprintf(
				'https://www.linkedin.com/shareArticle?mini=true&url=%1$s&title=%2$s',
				$post_link,
				get_the_title($post->ID)
			);
			break;

		default:
			return '';
	}
}

function gp_petrolimex_get_social_share_post($post_id)
{
	$types = array('facebook', 'pinterest', 'twitter', 'linkedin');
	$items = array();

	if (!empty($types)) {
		foreach ($types as $type) {
			$items[] = array(
				'type' => $type,
				'link_title' => sprintf(__('Share on %s', 'gp-petrolimex'), ucwords($type)),
				'svg_icon' => $type,
				'url' => gp_petrolimex_get_share_post_link(get_post($post_id), $type),
			);
		}
	}

	return $items;
}

